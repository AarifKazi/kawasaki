var x = document.querySelector(".dot-1")
var y = document.querySelector(".b0x")
y.style.display = "none";
x.addEventListener("click", function() {
    
    if (y.style.display === "none") {
        y.style.display = "flex";
    } else {
        y.style.display = "none";
    }
});

